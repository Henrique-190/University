def encontrar_palavras_comuns(listas):
    if len(listas) == 0:
        return []
    
    palavras_comuns = set([x.lower() for x in listas[0]])
    
    for lista in listas[1:]:
        lista_set = set([x.lower() for x in lista])
        palavras_comuns = palavras_comuns.intersection(lista_set)
    
    return list(palavras_comuns)

